package Day1;

public class CompareTwoNumbers {
    public static void main(String[] args) {
        int a = 6;
        int b = 1;

        if (a > b) {
            System.out.println(a + " is Max");
        }
        if (a == b) {
            System.out.println("a and b is Equal");
        }
        if (a < b) {
            System.out.println(b + " is Max");
        }
    }
}
